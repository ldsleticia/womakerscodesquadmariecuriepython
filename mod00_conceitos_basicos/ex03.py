quilometros = int(input('Digite os quilometros a converter: '))

metros = quilometros * 1000
centimetros = metros * 100
milimetros = centimetros * 10

print(f'A quilometragem fornecida em metros é {metros}m')
print(f'A quilometragem fornecida em centimetros é {centimetros}cm')
print(f'A quilometragem fornecida em milimetros é {milimetros}mm')
