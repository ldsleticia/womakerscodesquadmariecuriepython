numero_1 = int(input('Digite um número inteiro diferente de 0: '))
numero_2 = int(input('Digite um número inteiro diferente de 0: '))

soma = numero_1 + numero_2
subtracao = numero_1 - numero_2
multiplicacao = numero_1 * numero_2
divisao = numero_1 / numero_2

print(f'A soma dos números {numero_1} e {numero_2} é {soma}')
print(f'A subtração dos números {numero_1} e {numero_2} é {subtracao}')
print(f'A multiplicação dos números {numero_1} e {numero_2} é {multiplicacao}')
print(f'A divisão do número {numero_1} pelo {numero_2} é {divisao}')
